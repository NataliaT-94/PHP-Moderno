<?php
//while: funciona igual que el for. Solo que este evalua primero la si cumple con la condicion

$i = 11;

while($i < 10){
    echo $i;
    $i++;
}

//do while: por lo menos va a entrar la primera vez y si se cumple la condición va a seguir entrando mientras se siga cumpliendo

$j = 1;

do{
    echo "Entra";
} while($j < 10); //es la condicion que se tiene que cumplir para que retpita



?>